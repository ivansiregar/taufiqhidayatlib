package com.example.taufiqhidayatlib

class Calculate {

    companion object {
        fun tambah(i1: Int, i2: Int): Int {
            return (i1 + i2)
        }

        fun kali(i1: Int, i2: Int): Int {
            return (i1 * i2)
        }

        fun sub(i1: Int, i2: Int): Int {
            return (i1 - i2)
        }
    }
}